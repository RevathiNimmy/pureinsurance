CREATE PROCEDURE spu_PFPremiumFinance_Sel_For_PlanRef
	@PlanReference VARCHAR(20),
	@nPFPremfinancecnt INT= 0

AS
  IF @nPFPremfinancecnt = 0
	SELECT 1
	FROM   PFMediaTypeHistory
	WHERE  AutoGeneratedPlanRef = @PlanReference
	UNION
	SELECT 1
	FROM   PFPremiumFinance
	WHERE  AutoGeneratedPlanRef = @PlanReference
  ELSE
	SELECT 1
	FROM   PFPremiumFinance
	WHERE  AutoGeneratedPlanRef = @PlanReference
		   AND pfPrem_finance_cnt <> @nPFPremfinancecnt 
