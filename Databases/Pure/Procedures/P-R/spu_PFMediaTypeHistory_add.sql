CREATE PROCEDURE spu_PFMediaTypeHistory_add
@pfprem_finance_cnt int,
@pfprem_finance_version int,
@action_code varchar(50)
AS

IF @action_code = 'Setup'

BEGIN
 DECLARE @count int
 -- determine if we need to store the original details as the setup line in the history
 SELECT @count = count(*) FROM pfMediaTypeHistory
 WHERE pfprem_finance_cnt = @pfprem_finance_cnt
 AND pfprem_finance_version = @pfprem_finance_version

 IF @Count <> 0
  RETURN
END

INSERT INTO pfMediaTypeHistory (
pfprem_finance_cnt,
pfprem_finance_version,
mediatype_validation_code,
action_code,
BankAccountName,
BankSortCode,
BankAccountNo,
BankName,
BankBranch,
BankAddr1,
BankAddr2,
BankAddr3,
BankTown,
BankPCode,
BankRegion,
BankCountry,
BankAreaCode,
BankPhoneNo,
BankExtension,
BankFaxAreaCode,
BankFaxNo,
cc_number,
cc_expiry_date,
cc_start_date,
cc_issue,
cc_pin,
cardholder_name,
cardholder_address1,
cardholder_address2,
cardholder_address3,
cardholder_address4,
cardholder_postcode,
user_id,
date_modified,
paperdd,
AccountType,
PaymentType,
AutoGeneratedPlanRef,
deposit_cc_tracking_number,
business_identifier_code,
international_bank_account_number)

SELECT pfprem_finance_cnt,
pfprem_finance_version,
mediatype_validation.code,
@action_code,
LTRIM(RTRIM(BankAccountName)),
LTRIM(RTRIM(BankSortCode)),
LTRIM(RTRIM(BankAccountNo)),
LTRIM(RTRIM(BankName)),
LTRIM(RTRIM(BankBranch)),
LTRIM(RTRIM(BankAddr1)),
LTRIM(RTRIM(BankAddr2)),
LTRIM(RTRIM(BankAddr3)),
LTRIM(RTRIM(BankTown)),
LTRIM(RTRIM(BankPCode)),
LTRIM(RTRIM(BankRegion)),
LTRIM(RTRIM(BankCountry)),
LTRIM(RTRIM(BankAreaCode)),
LTRIM(RTRIM(BankPhoneNo)),
LTRIM(RTRIM(BankExtension)),
LTRIM(RTRIM(BankFaxAreaCode)),
LTRIM(RTRIM(BankFaxNo)),
LTRIM(RTRIM(cc_number)),
LTRIM(RTRIM(pfpremiumfinance.cc_expiry_date)),
LTRIM(RTRIM(pfpremiumfinance.cc_start_date)),
LTRIM(RTRIM(cc_issue)),
LTRIM(RTRIM(pfpremiumfinance.cc_pin)),
LTRIM(RTRIM(cardholder_name)),
LTRIM(RTRIM(cardholder_address1)),
LTRIM(RTRIM(cardholder_address2)),
LTRIM(RTRIM(cardholder_address3)),
LTRIM(RTRIM(cardholder_address4)),
LTRIM(RTRIM(cardholder_postcode)),
user_id,
date_modified,
paperdd,
LTRIM(RTRIM(PB.Account_type)),
LTRIM(RTRIM(ISNULL(cap.caption, BPT.description))) AS 'PaymentType',
AutoGeneratedPlanRef,
deposit_cc_tracking_number,
pfpremiumfinance.business_identifier_code,
pfpremiumfinance.international_bank_account_number
FROM pfpremiumfinance
INNER JOIN pfscheme ON
 pfscheme.schemeno = pfpremiumfinance.schemeno
 AND pfscheme.schemeversion = pfpremiumfinance.schemeversion
 AND pfscheme.companyno = pfpremiumfinance.companyno
INNER JOIN mediatype ON
 mediatype.mediatype_id = pfscheme.mediatype_id
INNER JOIN mediatype_validation ON
 mediatype_validation.mediatype_validation_id = mediatype.mediatype_validation_id
LEFT JOIN Party_Bank PB ON
 pfpremiumfinance.Party_Bank_Id=PB.Party_Bank_Id
LEFT JOIN bank_payment_type BPT ON
      PB.bank_payment_type_id = BPT.bank_payment_type_id
LEFT JOIN pmcaption cap ON
      BPT.caption_id = cap.caption_id And cap.language_id =1
WHERE pfprem_finance_cnt = @pfprem_finance_cnt
AND pfprem_finance_version = @pfprem_finance_version


SET QUOTED_IDENTIFIER OFF
GO
