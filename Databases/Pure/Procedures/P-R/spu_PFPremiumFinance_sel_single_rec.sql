        ISNULL(s.Spread_Commission, 0) AS Spread_Commission,
        ISNULL(s.spread_taxes, 0) as Spread_Taxes, 
        ISNULL(s.spread_ri, 0) as Spread_RI, 
        ISNULL(s.deposit_as_instalment, 0) as Deposit_As_Instalment,
        ISNULL(P.auth_code,'') as auth_code,
	ISNULL(P.pfprem_business_code,'') as pfprem_business_code,
	p.terms,
	p.sgref,
        p.sgdocurl,
        p.originalrate,
        p.sgrefundtype,
        p.limitedcompany,
	p.is_cardholder,
	p.cardholder_name,
	p.cardholder_address1,
	p.cardholder_address2,
	p.cardholder_address3,
	p.cardholder_address4,
	p.cardholder_postcode,
