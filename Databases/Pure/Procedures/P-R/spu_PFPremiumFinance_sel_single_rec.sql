        m.mediatype_validation_id = mv.mediatype_validation_id
    INNER JOIN
        pfScheme_type pfs
    ON
        pfs.pfScheme_type_id = s.pfScheme_type_id
    LEFT OUTER JOIN
        country bc
    ON
        bc.description = p.bankcountry
    LEFT OUTER JOIN
        country cc
    ON
        cc.description = p.clientcountry
    LEFT OUTER JOIN
	Insurance_File AS INF
    ON
	P.Insurance_File_Cnt = INF.insurance_file_cnt
    LEFT JOIN
    	PFScheme_PrintType AS PFSPT
    ON
    S.pfscheme_printtype_id = PFSPT.pfscheme_printtype_id
    WHERE
	claim_debt_id = @claim_debt_id
END

