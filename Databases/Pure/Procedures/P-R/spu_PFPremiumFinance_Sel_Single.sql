    DECLARE @TransCount int  
	DECLARE @crSettleAmount NUMERIC(19,4) 
	DECLARE @crRefundFee NUMERIC(19,4)
	DECLARE @crRefundTax NUMERIC(19,4)
	DECLARE @dtNextInstalmentDate DATETIME
	DECLARE @dtNextInstalmentDatePlus1 DATETIME
	DECLARE @dtLastInstalmentDate DATETIME
	DECLARE @dtLastPaidInstalmentDate DATETIME
	DECLARE @bCan_update_instalment_status TINYINT

