		ISNULL(@crSettleAmount,0) AS 'SettleAmount',
		p.deposit_cc_tracking_number,
        @bCan_update_instalment_status As can_update_instalment_status,
		s.receipt_difference_option
		FROM PFPremiumFinance P WITH(NOLOCK)  
		INNER JOIN PFScheme S WITH(NOLOCK) 
		ON S.companyno = P.companyno  
		AND S.schemeno = P.schemeno  
		AND S.schemeversion = P.schemeversion  
		INNER JOIN PFRF as PF WITH(NOLOCK)  
		ON PF.pfrf_id = P.pfrf_id  
		INNER JOIN PFFrequency PFF WITH(NOLOCK) 
		ON PFF.pffrequency_id = PF.pFfrequency_id  
		INNER JOIN mediatype m WITH(NOLOCK) 
		ON s.mediatype_id = m.mediatype_id  
		INNER JOIN mediatype_validation mv WITH(NOLOCK) 
		ON m.mediatype_validation_id = mv.mediatype_validation_id  
		INNER JOIN pfScheme_type pfs WITH(NOLOCK) 
		ON pfs.pfScheme_type_id = s.pfScheme_type_id  
		LEFT OUTER JOIN country bc WITH(NOLOCK) 
		ON bc.description = p.bankcountry  
		LEFT OUTER JOIN country cc WITH(NOLOCK) 
		ON cc.description = p.clientcountry  
		LEFT OUTER JOIN Insurance_File AS INF  
		ON P.Insurance_File_Cnt = INF.insurance_file_cnt  
		LEFT JOIN PFScheme_PrintType AS PFSPT WITH(NOLOCK) 
		ON S.pfscheme_printtype_id = PFSPT.pfscheme_printtype_id  
		JOIN Party as Pty WITH(NOLOCK) 
		ON pty.party_cnt = p.clientid  
		JOIN Party_type as PtyType WITH(NOLOCK)  
		ON PtyType.Party_Type_id = pty.party_type_id
		LEFT JOIN PFPremiumFinance_Cancel_Reason can ON can.pfpremiumfinance_cancel_reason_id = p.pfpremiumfinance_cancel_reason_id
		LEFT OUTER JOIN country chc WITH(NOLOCK) 
		ON chc.description = p.CardHolderCountry   
    
    WHERE  
				pfprem_finance_cnt = @financeplancnt
    AND  
				pfprem_finance_version = @financeplanversion	

		AND (p.insurance_file_cnt = @insurancefilecnt OR @insurancefilecnt IS NULL)

END  

