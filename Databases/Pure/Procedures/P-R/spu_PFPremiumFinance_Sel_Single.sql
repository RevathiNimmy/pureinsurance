    SELECT  
        @TransCount = Count(pFTransaction_id)  
    FROM pftransaction_id  
    WHERE pfprem_finance_cnt = @financeplancnt  
    AND  
        pfprem_finance_version = @financeplanversion
		SELECT @bCan_update_instalment_status=ISNull(can_update_instalment_status,0) FROM User_Authorities WHERE USER_ID=@nUserID

    SELECT  

		P.pfPrem_finance_cnt, 
		P.pfPrem_finance_version, 
		P.companyno, 
		P.companyname, 
		P.schemeno,
		P.schemename, 
		P.schemeversion, 
		P.startdate, 
		P.enddate, 
		P.productclass,
		P.transtype, 
		P.amounttofinance, 
		P.APR, 
		P.InterestRate, 
		P.DaysDelay,
		P.NoOfInstallments, 
		P.FirstInstallment, 
		P.OthInstallments, 
		P.CostOfProtection, 
		P.Deposit,
		P.NetAmount, 
		P.TotalCost, 
		P.InterestCost, 
		P.MinFinanceChrge, 
		P.PayProtection,
		S.QuoteDocID, 
		S.BankDocID, 
		S.CreditDocID, 
		P.ClientName, 
		P.ClientAddr1,
		P.ClientAddr2, 
		P.ClientAddr3, 
		P.ClientTown, 
		P.ClientAddr4, 
		P.ClientPcode,
		P.ClientCountry, 
		P.ClientAreaCode, 
		P.ClientPhoneNo, 
		P.ClientExtension, 
		P.ClientFaxAreaCode,
		P.ClientFaxNo, 
		P.BankName, 
		P.BankSortCode, 
		P.BankAccountNo, 
		P.BankAccountName,
		P.BankBranch, 
		P.BankAddr1, 
		P.BankAddr2, 
		P.BankAddr3, 
		P.BankTown,
		P.BankRegion, 
		P.BankPCode, 
		P.BankCountry, 
		P.BankAreaCode, 
		P.BankPhoneNo,
		P.BankExtension, 
		P.BankFaxAreaCode, 
		P.BankFaxNo, 
		NULL BrokerName, 
		NULL BrokerAddr1,
		NULL BrokerAddr2, 
		NULL BrokerAddr3, 
		NULL BrokerPCode, 
		NULL BrokerAddr4, 
		P.StatusInd,
		NULL NBInd, 
		NULL MTAInd, 
		NULL RNWLInd, 
		NULL CNCLInd, 
		NULL PPInd,
		P.ClientId, 
		P.ClientCode, 
		P.systemtag, 
		P.AutoGeneratedPlanRef, 
		P.FinanceCollatedPlanRef,
		NULL PolicyCnt, 
		P.InterestFree, 
		P.IsQuote, 
		P.IsParentPlan, 
		P.Parent_finance_cnt,
		P.Parent_finance_version, 
		P.PlanTransaction_id, 
		@TransCount TransCount, 
		S.ImmediateBankDetails,
		P.Insurance_File_Cnt, 
		P.PFRF_ID, 
		PFF.Code, 
		s.MediaType_ID, 
		s.PFScheme_type_id,
		bc.Country_id AS bank_country, 
		cc.Country_id AS client_country, 
		p.first_instalment_date, 
		p.next_instalment_date,
		p.last_instalment_date, 
		p.tax_cost, 
		m.code AS mediatype_code,
		p.cc_number,
		p.cc_expiry_date,
		p.cc_start_date,
		p.cc_issue,
		p.cc_pin, 
		mv.code AS MediaType_validation_code,
        ISNULL(s.bank_name_mandatory,0) AS bank_name_mandatory,  
        ISNULL(s.bank_address_mandatory,0) AS bank_address_mandatory,  
        ISNULL(s.branch_name_mandatory,0) AS branch_name_mandatory,  
		ISNULL(s.branch_code_mandatory,0) AS branch_code_mandatory,
		commission_transdetail_id,
		PFF.period, 
		PFF.amount, 
		PFF.pffrequency_id, 
		P.source_id, 
		INF.product_id, 
		pfs.code AS PFScheme_Type_Code,
		ISNULL(P.FinanceFee, 0) AS FinanceFee, 
		ISNULL(P.DayOfWeekOrMonth, 0) AS DayOfWeekOrMonth,
