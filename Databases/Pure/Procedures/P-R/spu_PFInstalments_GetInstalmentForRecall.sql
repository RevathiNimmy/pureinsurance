CREATE PROCEDURE spu_PFInstalments_GetInstalmentForRecall
    @pfinstalments_id INT

AS

/*
    This stored procedure mimics the data structure returned
    by the Finance Spoke so the interface iPMBExport and Posting
    routines can share the code
*/

SELECT
			0,          --Detailid
			0,          --Record Status
			'',         --Record Message
			PTY.shortname,
			P.clientname,
			P.bankaccountname,
			I.amount,
			'',         --Insurance File Ref
			P.bankaccountno,
			P.banksortcode,
			P.bankname,
			P.clientname,
			P.cc_number,
			P.cc_expiry_date,
			P.cc_start_date,
			P.cc_issue,
			P.cc_pin,
			I.pftransaction_id,
			PTY.alternative_identifier,
			PTY.agent_cnt,
			0,          -- Group ID
			I.pfinstalments_id,
			I.InstalmentNumber,
			I.Status,
			S.Description,
			T.code,
			P.AutoGeneratedPlanRef,
			P.plantransaction_id
FROM
    PFInstalments I
INNER JOIN
    PFPremiumFinance P
ON
    P.pfprem_finance_cnt = I.pfprem_finance_cnt
AND P.pfprem_finance_version = I.pfprem_finance_version
INNER JOIN
    Party PTY
ON
    P.clientid=PTY.party_cnt
INNER JOIN
        PFInstalments_status S
    ON
        I.Status = S.PFInstalments_status_id
INNER JOIN
        PFInstalments_transaction T
    ON
        I.TransactionCode = T.PFInstalments_transaction_id
WHERE
    I.pfinstalments_id=@pfinstalments_id
AND I.Status=3
Go

