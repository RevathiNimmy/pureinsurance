CREATE PROCEDURE spu_Check_Third_Party_Instalment
@PlanRef Varchar(20)
AS

SELECT pfp.pfprem_finance_cnt from pfpremiumfinance pfp
JOIN pfscheme pfs
ON pfs.companyno = pfp.companyno
AND pfs.schemeno = pfp.schemeno
AND pfs.schemeversion = pfp.schemeversion
WHERE pfp.AutoGeneratedPlanRef  = @PlanRef and pfs.pfscheme_type_id =1

GO