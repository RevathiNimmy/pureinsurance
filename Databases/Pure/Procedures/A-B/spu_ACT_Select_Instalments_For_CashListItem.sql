--DC290704 PN11529 bring into line with spu_ACT_Select_Instalments_For_Account
CREATE PROCEDURE spu_ACT_Select_Instalments_For_CashListItem
@CashListItem_id int
AS
select pfi.pfinstalments_id, pfi.pfprem_finance_cnt, pfi.pfprem_finance_version,
pfi.instalmentnumber, pfi.duedate,
pfi.fee, pfi.amount,
pfp.autogeneratedplanref, convert(tinyint,1) as seletectedflag,
convert(tinyint,0) as PartialPayment,
pfi.TransactionCode
from
pfinstalments pfi, cashlistitem_instalments cli, pfpremiumfinance pfp
where
pfp.pfprem_finance_cnt = pfi.pfprem_finance_cnt
and
pfp.pfprem_finance_version = pfi.pfprem_finance_version
and
pfi.pfinstalments_id = cli.pfinstalments_id
and
cli.cashlistitem_id = @CashListItem_id
GO
