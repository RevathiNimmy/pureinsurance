CREATE PROCEDURE spu_ACT_Select_Instalments_For_PartySettelment  
	@account_id INT,
	@viewtype CHAR(3)    
AS BEGIN
  
	DECLARE @AgentCnt INT    
    
	SELECT @AgentCnt = PF.agent_cnt FROM account A    
	INNER JOIN pfpremiumfinance PF ON A.account_key = PF.agent_cnt    
	WHERE A.account_id = @account_id    
    
	IF ISNULL(@AgentCnt,0) > 0 
	BEGIN    
		SELECT    
			pfi.pfinstalments_id,    
			pfi.pfprem_finance_cnt,    
			pfi.pfprem_finance_version,    
			pfi.instalmentnumber,    
			pfi.duedate,    
			pfi.fee,    
			pfi.amount,    
			pfp.autogeneratedplanref,    
			1 as SelectedFlag,
			CASE WHEN @viewtype = 'TRN' THEN tm.base_match_amount --[TRN - View using Transaction Currency]
			  WHEN @viewtype = 'ACC' THEN tm.account_match_amount --[ACC - View using Account Currency]
			END [PartialPayment],  
			pfi.TransactionCode,    
			pfs.receipt_difference_option,
			ISNULL(pfp.use_trans_currency,0),
			ifi.currency_id
		FROM account a    
		JOIN pfpremiumfinance pfp ON pfp.agent_cnt = a.account_key    
		JOIN pfinstalments pfi    ON pfi.pfprem_finance_cnt = pfp.pfprem_finance_cnt    
				AND pfi.pfprem_finance_version = pfp.pfprem_finance_version    
		JOIN pfinstalments_status pfis  ON pfis.pfinstalments_status_id = pfi.Status    
		JOIN pfscheme pfs    ON pfs.companyno = pfp.companyno    
				AND pfs.schemeno = pfp.schemeno    
				AND pfs.schemeversion = pfp.schemeversion    
		Join document d with (nolock)   On pfp.insurance_file_cnt=d.insurance_file_cnt          
		Join transdetail td with (nolock) On td.document_id = d.document_id  
		Join transmatch tm   On td.transdetail_id=tm.transdetail_id and tm.instalmentnumber=pfi.instalmentnumber
		JOIN Insurance_File ifi
			ON ifi.insurance_file_cnt = pfp.Insurance_File_Cnt            
		WHERE a.account_id = @account_id    
		AND pfp.StatusInd = '040'    
		AND (pfis.code = 'U' OR pfis.code = 'R' OR pfis.code = 'F' OR pfis.code = 'M' OR pfis.code = 'P')    
		ORDER BY pfp.pfprem_finance_cnt,pfi.instalmentnumber, pfi.pfinstalments_id    
		
	END ELSE BEGIN    
	
		SELECT    
			pfi.pfinstalments_id,    
			pfi.pfprem_finance_cnt,    
			pfi.pfprem_finance_version,    
			pfi.instalmentnumber,    
			pfi.duedate,    
			pfi.fee,    
			pfi.amount,    
			pfp.autogeneratedplanref,    
			1 as SelectedFlag,
			CASE WHEN @viewtype = 'TRN' THEN tm.base_match_amount --[TRN - View using Transaction Currency]
				 WHEN @viewtype = 'ACC' THEN tm.account_match_amount --[ACC - View using Account Currency]
			END [PartialPayment],         
			pfi.TransactionCode,    
			pfs.receipt_difference_option,  			
			td.transdetail_id,			--- Additional column is returned...	
			ISNULL(pfp.use_trans_currency,0),
			ifi.currency_id
		FROM account a    
		JOIN pfpremiumfinance pfp  ON pfp.clientid = a.account_key  
		JOIN pfinstalments pfi     ON pfi.pfprem_finance_cnt = pfp.pfprem_finance_cnt    
			AND pfi.pfprem_finance_version = pfp.pfprem_finance_version    
		JOIN pfinstalments_status pfis   ON pfis.pfinstalments_status_id = pfi.Status    
		JOIN pfscheme pfs    ON pfs.companyno = pfp.companyno    
			AND pfs.schemeno = pfp.schemeno    
			AND pfs.schemeversion = pfp.schemeversion    
		Join document d with (nolock)   On pfp.insurance_file_cnt=d.insurance_file_cnt          
		Join transdetail td with (nolock) On td.document_id = d.document_id  
		Join transmatch tm   On td.transdetail_id=tm.transdetail_id 
			and tm.instalmentnumber=pfi.instalmentnumber
		JOIN Insurance_File ifi
			ON ifi.insurance_file_cnt = pfp.Insurance_File_Cnt             
		WHERE a.account_id = @account_id    
		AND pfp.StatusInd = '040'    
		AND (pfis.code = 'U' OR pfis.code = 'R' OR pfis.code = 'F' OR pfis.code = 'M' OR pfis.code = 'P')    
		--AND ISNULL(pfp.agent_cnt,0)=0      
		ORDER BY pfp.pfprem_finance_cnt,pfi.instalmentnumber, pfi.pfinstalments_id    
	END  
END
GO
SET QUOTED_IDENTIFIER OFF 
GO
SET ANSI_NULLS ON 
