CREATE PROCEDURE spu_ACT_Import_Get_Instalment_Plan  
@plan_ref varchar(20),  
@cc_number varchar(30),  
@banksortcode varchar(8),  
@bankaccountno varchar(30),  
@pfpremfinancecnt int OUTPUT,  
@pfpremfinanceversion int OUTPUT,  
@statusind varchar(3) OUTPUT,  
@ddcancelled tinyint OUTPUT,  
@cccancelled tinyint OUTPUT,  
@planBankAccountName varchar(30) OUTPUT,  
@cover_start_date datetime OUTPUT,  
@expiry_date datetime OUTPUT,  
@insurance_file_status_id int OUTPUT,  
@insurance_file_cnt int OUTPUT, 
@insured_shortname varchar(20) OUTPUT,
@instalment_group_id Int
AS  
DECLARE @enumStatusLive         INT = 040
		,@enumStatusOnHold      INT = 140
		,@enumStatusCompleted   INT = 900 
		,@enumStatusSuperseded   INT = 990
		,@enumStatusCancelled   INT = 999
BEGIN 

DECLARE @bPlanExist  bit =0

IF NOT @cc_number IS NULL 
BEGIN
	IF NOT EXISTS(SELECT pfprem_finance_cnt FROM pfpremiumfinance WHERE AutoGeneratedPlanRef = @plan_ref AND cc_number =@cc_number AND StatusInd IN (@enumStatusLive,@enumStatusOnHold,@enumStatusCompleted,@enumStatusCancelled,@enumStatusSuperseded))
	BEGIN
	IF EXISTS(SELECT pfprem_finance_cnt FROM PFMediaTypeHistory WHERE AutoGeneratedPlanRef = @plan_ref AND cc_number = @cc_number)
		SET @bPlanExist=1
	END
	ELSE
		SET	@bPlanExist=1

IF (@bPlanExist=1)
SELECT  
@pfpremfinancecnt = pfpremiumfinance.pfprem_finance_cnt,  
@pfpremfinanceversion = pfpremiumfinance.pfprem_finance_version,  
@statusind = statusind ,  
@ddcancelled = dd_cancelled,  
@cccancelled = cc_cancelled,  
@planBankAccountName = bankaccountname ,  
@cover_start_date = cover_start_date,  
@expiry_date = expiry_date,  
@insurance_file_status_id = insurance_file_status_id,  
@insurance_file_cnt = insurance_file.insurance_file_cnt, 
@insured_shortname = party.shortname 

FROM pfpremiumfinance  
INNER JOIN insurance_file ON  
	pfpremiumfinance.insurance_file_cnt = insurance_file.insurance_file_cnt  

INNER JOIN Party ON 
	insurance_file.insured_cnt = party.party_cnt
INNER JOIN PFInstalments PFI ON pfpremiumfinance.pfprem_finance_cnt=PFI.pfprem_finance_cnt AND pfpremiumfinance.pfprem_finance_version=PFI.pfprem_finance_version
WHERE AutoGeneratedPlanRef = @plan_ref
	AND StatusInd IN (@enumStatusLive,@enumStatusOnHold,@enumStatusCompleted,@enumStatusCancelled,@enumStatusSuperseded)
	AND PFI.group_id=@instalment_group_id
ORDER BY pfpremiumfinance.pfprem_finance_cnt 
  
END
ELSE BEGIN
  
IF NOT EXISTS(SELECT pfprem_finance_cnt FROM pfpremiumfinance WHERE AutoGeneratedPlanRef = @plan_ref AND banksortcode = @banksortcode AND bankaccountno = @bankaccountno AND StatusInd IN (@enumStatusLive,@enumStatusOnHold,@enumStatusCompleted,@enumStatusCancelled,@enumStatusSuperseded))
BEGIN  
	IF EXISTS(SELECT pfprem_finance_cnt FROM PFMediaTypeHistory WHERE AutoGeneratedPlanRef = @plan_ref AND banksortcode = @banksortcode AND bankaccountno = @bankaccountno)
		SET @bPlanExist=1
END
ELSE
		SET	@bPlanExist=1
  
IF (@bPlanExist=1)
 SELECT  
			@pfpremfinancecnt = pfpremiumfinance.pfprem_finance_cnt,  
			@pfpremfinanceversion = pfpremiumfinance.pfprem_finance_version,  
  @statusind = statusind ,  
  @ddcancelled = dd_cancelled,  
  @cccancelled = cc_cancelled,  
  @planBankAccountName = bankaccountname ,  
  @cover_start_date = cover_start_date,  
  @expiry_date = expiry_date,  
  @insurance_file_status_id = insurance_file_status_id,  
  @insurance_file_cnt = insurance_file.insurance_file_cnt, 
  @insured_shortname = party.shortname 
  
 FROM pfpremiumfinance  
  
 INNER JOIN insurance_file ON  
  pfpremiumfinance.insurance_file_cnt = insurance_file.insurance_file_cnt  

	INNER JOIN Party ON 
		insurance_file.insured_cnt = party.party_cnt
	INNER JOIN PFInstalments PFI ON pfpremiumfinance.pfprem_finance_cnt=PFI.pfprem_finance_cnt AND pfpremiumfinance.pfprem_finance_version=PFI.pfprem_finance_version
  
	WHERE AutoGeneratedPlanRef = @plan_ref  
	AND StatusInd IN (@enumStatusLive,@enumStatusOnHold,@enumStatusCompleted,@enumStatusCancelled,@enumStatusSuperseded)
	AND PFI.group_id=@instalment_group_id
	ORDER BY pfpremiumfinance.pfprem_finance_cnt 
END  

END


GO
