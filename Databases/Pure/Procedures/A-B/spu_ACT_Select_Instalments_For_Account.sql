CREATE PROCEDURE spu_ACT_Select_Instalments_For_Account
	@account_id INT,
	@third_party VARCHAR(10)=''
AS

DECLARE @AgentCnt INT

	SELECT @AgentCnt = PF.agent_cnt FROM account A
	INNER JOIN pfpremiumfinance PF ON A.account_key = PF.agent_cnt
	WHERE A.account_id = @account_id

IF ISNULL(@AgentCnt,0)>0 --PN 50319
BEGIN
IF @third_party = 'TPPF'
BEGIN
SELECT
	ISNULL(pfi.pfinstalments_id,-1),
	pfi.pfprem_finance_cnt,
	pfi.pfprem_finance_version,
	ISNULL(pfi.instalmentnumber,-1),
	pfi.duedate,
	pfi.fee,
	pfp.NetAmount,
	pfp.autogeneratedplanref,
	CONVERT(TINYINT,0) as SelectedFlag,
	CONVERT(TINYINT,0) as PartialPayment,
	ISNULL(pfi.TransactionCode,0),
	pfs.receipt_difference_option,
	ISNULL(pfp.use_trans_currency,0),
	ifi.currency_id,
	0 AS InstalmentReceiptAmount,
	1 AS Xrate
FROM account a
JOIN pfpremiumfinance pfp
	ON pfp.agent_cnt = a.account_key
LEFT JOIN pfinstalments pfi
	ON pfi.pfprem_finance_cnt = pfp.pfprem_finance_cnt
	AND pfi.pfprem_finance_version = pfp.pfprem_finance_version
LEFT JOIN pfinstalments_status pfis
	ON pfis.pfinstalments_status_id = pfi.Status
JOIN pfscheme pfs
	ON pfs.companyno = pfp.companyno
	AND pfs.schemeno = pfp.schemeno
	AND pfs.schemeversion = pfp.schemeversion
JOIN Insurance_File ifi
	ON ifi.insurance_file_cnt = pfp.Insurance_File_Cnt
WHERE a.account_id = @account_id
AND pfp.StatusInd = '040'
AND pfs.pfscheme_type_id=1
ORDER BY pfi.instalmentnumber, pfi.pfinstalments_id
END
ELSE
BEGIN
SELECT
	pfi.pfinstalments_id,
	pfi.pfprem_finance_cnt,
	pfi.pfprem_finance_version,
	pfi.instalmentnumber,
	pfi.duedate,
	pfi.fee,
	pfi.amount,
	pfp.autogeneratedplanref,
	0 as SelectedFlag,
	0 as PartialPayment,
	pfi.TransactionCode,
	pfs.receipt_difference_option,
	ISNULL(pfp.use_trans_currency,0),
	ifi.currency_id,
	0 AS InstalmentReceiptAmount,
	1 AS Xrate,
	ISNULL(pfi.write_off_reason_id,0)
FROM account a
JOIN pfpremiumfinance pfp
	ON pfp.agent_cnt = a.account_key
JOIN pfinstalments pfi
	ON pfi.pfprem_finance_cnt = pfp.pfprem_finance_cnt
	AND pfi.pfprem_finance_version = pfp.pfprem_finance_version
JOIN pfinstalments_status pfis
	ON pfis.pfinstalments_status_id = pfi.Status
JOIN pfscheme pfs
	ON pfs.companyno = pfp.companyno
	AND pfs.schemeno = pfp.schemeno
	AND pfs.schemeversion = pfp.schemeversion
JOIN Insurance_File ifi
	ON ifi.insurance_file_cnt = pfp.Insurance_File_Cnt
WHERE a.account_id = @account_id
AND pfp.StatusInd = '040'
AND (pfis.code = 'U' OR pfis.code = 'R' OR pfis.code = 'F' OR pfis.code = 'M')
AND pfi.amount > 0
ORDER BY pfi.instalmentnumber, pfi.pfinstalments_id
END
END
ELSE
BEGIN
IF @third_party = 'TPPF'
BEGIN
SELECT 
	ISNULL(pfi.pfinstalments_id,-1),
	pfi.pfprem_finance_cnt,
	pfi.pfprem_finance_version,
	ISNULL(pfi.instalmentnumber,-1),
	pfi.duedate,
	pfi.fee,
	pfp.NetAmount,
	pfp.autogeneratedplanref,
	CONVERT(TINYINT,0) as SelectedFlag,
	CONVERT(TINYINT,0) as PartialPayment,
	ISNULL(pfi.TransactionCode,0),
	pfs.receipt_difference_option,
	ISNULL(pfp.use_trans_currency,0),
	ifi.currency_id,
	0 AS InstalmentReceiptAmount,
	1 AS Xrate
FROM account a
JOIN pfpremiumfinance pfp
	ON pfp.clientid = a.account_key
LEFT JOIN pfinstalments pfi
	ON pfi.pfprem_finance_cnt = pfp.pfprem_finance_cnt
	AND pfi.pfprem_finance_version = pfp.pfprem_finance_version
LEFT JOIN pfinstalments_status pfis
	ON pfis.pfinstalments_status_id = pfi.Status
JOIN pfscheme pfs
	ON pfs.companyno = pfp.companyno
	AND pfs.schemeno = pfp.schemeno
	AND pfs.schemeversion = pfp.schemeversion
JOIN Insurance_File ifi
	ON ifi.insurance_file_cnt = pfp.Insurance_File_Cnt
WHERE a.account_id = @account_id
AND pfp.StatusInd = '040'
AND ISNULL(pfp.agent_cnt,0)=0
AND pfs.pfscheme_type_id=1
ORDER BY pfi.instalmentnumber, pfi.pfinstalments_id
END
ELSE
BEGIN
SELECT
	pfi.pfinstalments_id,
	pfi.pfprem_finance_cnt,
	pfi.pfprem_finance_version,
	pfi.instalmentnumber,
	pfi.duedate,
	pfi.fee,
	pfi.amount,
	pfp.autogeneratedplanref,
	0 as SelectedFlag,
	0 as PartialPayment,
	pfi.TransactionCode,
	pfs.receipt_difference_option,
	ISNULL(pfp.use_trans_currency,0),
	ifi.currency_id,
	0 AS InstalmentReceiptAmount,
	1 AS Xrate,
	ISNULL(pfi.write_off_reason_id,0)
FROM account a
JOIN pfpremiumfinance pfp
	ON pfp.clientid = a.account_key
JOIN pfinstalments pfi
	ON pfi.pfprem_finance_cnt = pfp.pfprem_finance_cnt
	AND pfi.pfprem_finance_version = pfp.pfprem_finance_version
JOIN pfinstalments_status pfis
	ON pfis.pfinstalments_status_id = pfi.Status
JOIN pfscheme pfs
	ON pfs.companyno = pfp.companyno
	AND pfs.schemeno = pfp.schemeno
	AND pfs.schemeversion = pfp.schemeversion
JOIN Insurance_File ifi
	ON ifi.insurance_file_cnt = pfp.Insurance_File_Cnt
WHERE a.account_id = @account_id
AND pfp.StatusInd = '040'
AND (pfis.code = 'U' OR pfis.code = 'R' OR pfis.code = 'F' OR pfis.code = 'M')
AND ISNULL(pfp.agent_cnt,0)=0
AND pfi.amount > 0
ORDER BY pfi.instalmentnumber, pfi.pfinstalments_id
END
END

GO
