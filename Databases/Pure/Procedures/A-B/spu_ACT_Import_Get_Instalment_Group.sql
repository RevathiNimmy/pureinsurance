CREATE PROCEDURE spu_ACT_Import_Get_Instalment_Group

@autogeneratedplanref varchar(20),
@amount money, 
@group_id int OUTPUT

AS

SELECT TOP 1 @group_id = group_id

FROM pfinstalments 

	INNER JOIN pfpremiumfinance ON
		pfinstalments.pfprem_finance_cnt = pfpremiumfinance.pfprem_finance_cnt  
	   AND pfinstalments.pfprem_finance_version = 	pfpremiumfinance.pfprem_finance_version

WHERE group_id IS NOT NULL
GROUP BY group_id, autogeneratedplanref

HAVING 
pfpremiumfinance.autogeneratedplanref = @autogeneratedplanref
AND SUM(Round(amount,2)) = @amount

ORDER BY group_id desc




GO
