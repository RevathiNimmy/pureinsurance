CREATE PROCEDURE spe_PFPremiumFinance_sel_single
    @financeplancnt int,
    @financeplanversion int,
    @insurancefilecnt int=NULL

AS
    DECLARE @TransCount int

    /* Get the transaction count */
    SELECT
        @TransCount = Count(pFTransaction_id)
    FROM
        pftransaction_id
    WHERE
        pfprem_finance_cnt = @financeplancnt
    AND
        pfprem_finance_version = @financeplanversion

    /* Coded to match MC array constant definition in bSIRPremFinConst */
    SELECT
        P.pfPrem_finance_cnt, P.pfPrem_finance_version,

        P.companyno, P.companyname, P.schemeno, P.schemename, P.schemeversion,

        P.startdate, P.enddate, P.productclass, P.transtype, P.amounttofinance, P.APR, P.InterestRate,
        P.DaysDelay, P.NoOfInstallments, P.FirstInstallment, P.OthInstallments, P.CostOfProtection,
        P.Deposit, P.NetAmount, P.TotalCost, P.InterestCost, P.MinFinanceChrge, P.PayProtection,

        S.QuoteDocID, S.BankDocID, S.CreditDocID,

        P.ClientName, P.ClientAddr1, P.ClientAddr2, P.ClientAddr3, P.ClientTown, P.ClientAddr4,
        P.ClientPcode, P.ClientCountry, P.ClientAreaCode, P.ClientPhoneNo, P.ClientExtension,
        P.ClientFaxAreaCode, P.ClientFaxNo,

        P.BankName, P.BankSortCode, P.BankAccountNo, P.BankAccountName, P.BankBranch,
        P.BankAddr1, P.BankAddr2, P.BankAddr3, P.BankTown, P.BankRegion, P.BankPCode, P.BankCountry,
        P.BankAreaCode, P.BankPhoneNo, P.BankExtension, P.BankFaxAreaCode, P.BankFaxNo,

        Null BrokerName, Null BrokerAddr1, Null BrokerAddr2, Null BrokerAddr3, Null BrokerPCode, Null BrokerAddr4,

        P.StatusInd, Null NBInd, Null MTAInd, Null RNWLInd, Null CNCLInd, Null PPInd,

        P.ClientId, P.ClientCode, P.systemtag, P.AutoGeneratedPlanRef, P.FinanceCollatedPlanRef,
        Null PolicyCnt, P.InterestFree, P.IsQuote, P.IsParentPlan, P.Parent_finance_cnt,
        P.Parent_finance_version, P.PlanTransaction_id, @TransCount TransCount, S.IsInHouse, S.ImmediateBankDetails,
       	P.Insurance_File_Cnt

    FROM
        PFPremiumFinance P

    INNER JOIN
        PFScheme S
    ON
        S.companyno = P.companyno
    AND
        S.schemeno = P.schemeno
    AND
        S.schemeversion = P.schemeversion

    WHERE
       ((pfprem_finance_cnt = @financeplancnt OR @financeplancnt IS NULL)
    AND
        (pfprem_finance_version = @financeplanversion OR @financeplanversion IS NULL))
    AND
        (insurance_file_cnt = @insurancefilecnt OR @insurancefilecnt IS NULL)
GO

