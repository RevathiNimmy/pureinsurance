CREATE PROCEDURE spe_PFPremiumFinanceVersions_select
    @financecnt int
AS
BEGIN

SELECT
        P.pfPrem_finance_cnt, P.pfPrem_finance_version, P.companyno, P.companyname, P.schemeno,
        P.schemename, P.schemeversion, P.startdate, P.enddate, P.productclass,
        P.transtype, P.amounttofinance, P.APR, P.InterestRate, P.DaysDelay,
        P.NoOfInstallments, P.FirstInstallment, P.OthInstallments, P.CostOfProtection, P.Deposit,
        P.NetAmount, P.TotalCost, P.InterestCost, P.MinFinanceChrge, P.PayProtection,
        S.QuoteDocID, S.BankDocID, S.CreditDocID, P.ClientName, P.ClientAddr1,
        P.ClientAddr2, P.ClientAddr3, P.ClientTown, P.ClientAddr4, P.ClientPcode,
        P.ClientCountry, P.ClientAreaCode, P.ClientPhoneNo, P.ClientExtension, P.ClientFaxAreaCode,
        P.ClientFaxNo, P.BankName, P.BankSortCode, P.BankAccountNo, P.BankAccountName,
        P.BankBranch, P.BankAddr1, P.BankAddr2, P.BankAddr3, P.BankTown,
        P.BankRegion, P.BankPCode, P.BankCountry, P.BankAreaCode, P.BankPhoneNo,
        P.BankExtension, P.BankFaxAreaCode, P.BankFaxNo, Null BrokerName, Null BrokerAddr1,
        Null BrokerAddr2, Null BrokerAddr3, Null BrokerPCode, Null BrokerAddr4, P.StatusInd,
        Null NBInd, Null MTAInd, Null RNWLInd, Null CNCLInd, Null PPInd,
        P.ClientId, P.ClientCode, P.systemtag, P.AutoGeneratedPlanRef, P.FinanceCollatedPlanRef,
        Null PolicyCnt, P.InterestFree, P.IsQuote, P.IsParentPlan, P.Parent_finance_cnt,
        P.Parent_finance_version, P.PlanTransaction_id, (SELECT Count(*)
        FROM pftransaction_id PFT
        WHERE PFT.pfprem_finance_cnt = P.pfprem_finance_cnt
        AND PFT.pfprem_finance_version = P.pfprem_finance_version) TransCount, S.ImmediateBankDetails,
        P.Insurance_File_Cnt, P.PFRF_ID, PFF.Code, s.MediaType_ID, s.PFScheme_type_id,
        bc.Country_id as bank_country, cc.Country_id as client_country, p.first_instalment_date, p.next_instalment_date,
        p.last_instalment_date, p.tax_cost, m.code as mediatype_code,
        p.cc_number,p.cc_expiry_date,p.cc_start_date,p.cc_issue,p.cc_pin, mv.code as MediaType_validation_code,
        ISNULL(s.bank_name_mandatory,0) AS bank_name_mandatory,
    ISNULL(s.bank_address_mandatory,0) AS bank_address_mandatory,
    ISNULL(s.branch_name_mandatory,0) AS branch_name_mandatory,
    ISNULL(s.branch_code_mandatory,0) AS branch_code_mandatory,commission_transdetail_id,
    PFF.period, PFF.amount, PFF.pffrequency_id, P.source_id, INF.product_id, pfs.code as PFScheme_Type_Code,
    ISNULL(P.FinanceFee, 0) AS FinanceFee, ISNULL(P.DayOfWeekOrMonth, 0) AS DayOfWeekOrMonth,
        m.Description as PaymentMethod, pff.Description as Frequency, PFSPT.code AS SchemePrintType,
        p.original_amount, p.last_instalment, p.claim_debt_id, p.user_id, p.agent_cnt, p.agent_ref,
        p.date_created, p.date_modified, p.date_confirmed, p.date_review, p.date_laststatement, p.date_lastgeneration, 
        p.final_statement_set, p.no_statements, p.statement_pffrequency_id, p.review_pmwrk_task_instance_id, 
        ISNULL(m.is_via_third_party,0) AS is_via_third_party, PF.review_pmuser_group_id, S.ConfirmationDocID

        
    FROM
        PFPremiumFinance P
    INNER JOIN
        PFScheme S
    ON
        S.companyno = P.companyno
    AND
        S.schemeno = P.schemeno
    AND
        S.schemeversion = P.schemeversion

    INNER JOIN
    PFRF as PF
    ON
    PF.pfrf_id = P.pfrf_id

    INNER JOIN
    PFFrequency PFF
    ON
    PFF.pffrequency_id = PF.pFfrequency_id

    INNER JOIN
        mediatype m
    ON
        s.mediatype_id = m.mediatype_id

    INNER JOIN
        mediatype_validation mv
    ON
--PN11684       mv.mediatype_validation_id = mv.mediatype_validation_id
	mv.mediatype_validation_id = m.mediatype_validation_id
    INNER JOIN
        pfScheme_type pfs
    ON
        pfs.pfScheme_type_id = s.pfScheme_type_id

    LEFT OUTER JOIN
        country bc
    ON
        bc.description = p.bankcountry

    LEFT OUTER JOIN
        country cc
    ON
        cc.description = p.clientcountry
    LEFT OUTER JOIN
    Insurance_File AS INF
    ON
    P.Insurance_File_Cnt = INF.insurance_file_cnt
    
    INNER JOIN 
    	PFScheme_PrintType AS PFSPT
    ON
    S.pfscheme_printtype_id = PFSPT.pfscheme_printtype_id
WHERE
    P.pfPrem_finance_cnt = @financecnt

END
GO

