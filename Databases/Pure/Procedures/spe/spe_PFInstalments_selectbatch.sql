CREATE PROCEDURE spe_PFInstalments_selectbatch
    @BatchID INT,
    @ForRecall SMALLINT,
    @ForReview SMALLINT=0,
	@INCDocumentRef VARCHAR(40)=''
AS
/*
    This stored procedure mimics the data structure returned
    by the Finance Spoke so the interface iPMBExport and Posting
    routines can share the code
*/
IF @INCDocumentRef = ''  BEGIN
	SELECT
		0,          --Detailid
		0,          --Record Status
		'',         --Record Message
		PTY.shortname,
		P.clientname,
		P.bankaccountname,
		I.amount,
		'',         --Insurance File Ref
		P.bankaccountno,
		P.banksortcode,
		P.bankname,
		P.clientname,
		P.cc_number,
		P.cc_expiry_date,
		P.cc_start_date,
		P.cc_issue,
		P.cc_pin,
		I.pftransaction_id,
		PTY.alternative_identifier,
		PTY.agent_cnt,
		0,          -- Group ID
		I.pfinstalments_id,
		I.InstalmentNumber,
		I.Status,
		S.Description,
		T.code,
		P.AutoGeneratedPlanRef,
		ISNULL(P.plantransaction_id,0),
		ISNULL(p.VIAPaymentHub,0)
	FROM
		PFInstalments I
	INNER JOIN
		PFPremiumFinance P
	ON
		P.pfprem_finance_cnt = I.pfprem_finance_cnt
	AND P.pfprem_finance_version = I.pfprem_finance_version
	INNER JOIN
		Party PTY
	ON
		P.clientid=PTY.party_cnt
	INNER JOIN
			PFInstalments_status S
		ON
			I.Status = S.PFInstalments_status_id
	INNER JOIN
			PFInstalments_transaction T
		ON
			I.TransactionCode = T.PFInstalments_transaction_id
	WHERE
		I.batch_id=@BatchID
	AND ((I.Status<>3 AND @ForRecall=0 AND P.StatusInd<>140 AND s.code <> 'F')	-- for posting
	OR  ((I.Status<>3  AND ISNULL(I.pftransaction_id,0)=0 AND @ForRecall=0 AND @ForReview=1)      -- to preview posting
	OR  (I.Status<>3 AND @ForRecall=1 AND @ForReview=0)                       -- for recall
	OR  (I.Status=3 AND @ForRecall=1 )))                      		  -- to preview recall
END
IF @INCDocumentRef <> ''  BEGIN
SELECT
    0,          --Detailid
    0,          --Record Status
    '',         --Record Message
    PTY.shortname,
    P.clientname,
    PFMTH.bankaccountname,  
    I.amount,
    '',         --Insurance File Ref
    PFMTH.bankaccountno,  
    PFMTH.banksortcode,  
    PFMTH.bankname,
    P.clientname,
    P.cc_number,
    P.cc_expiry_date,
    P.cc_start_date,
    P.cc_issue,
    P.cc_pin,
    I.pftransaction_id,
    PTY.alternative_identifier,
    PTY.agent_cnt,
    0,          -- Group ID
    I.pfinstalments_id,
    I.InstalmentNumber,
    I.Status,
    S.Description,
    T.code,
    P.AutoGeneratedPlanRef,
    ISNULL(P.plantransaction_id,0),
	ISNULL(p.VIAPaymentHub,0)
FROM
    PFInstalments I
INNER JOIN
    PFPremiumFinance P
ON
    P.pfprem_finance_cnt = I.pfprem_finance_cnt
AND P.pfprem_finance_version = I.pfprem_finance_version
INNER JOIN
    Party PTY
ON
    P.clientid=PTY.party_cnt
INNER JOIN
        PFInstalments_status S
    ON
        I.Status = S.PFInstalments_status_id
INNER JOIN
        PFInstalments_transaction T
    ON
        I.TransactionCode = T.PFInstalments_transaction_id
INNER JOIN 
	PFMediaTypeHistory PFMTH
	ON 
		PFMTH.pfMediaTypeHistory_id = I.pfmediatype_history_id
WHERE
  I.pftransaction_id in (Select transdetail_id from transdetail where document_id = (select document_id from document where document_ref = @INCDocumentRef))
AND ((I.Status<>3 AND @ForRecall=0 AND P.StatusInd<>140 AND s.code <> 'F')	-- for posting
OR  ((I.Status<>3  AND ISNULL(I.pftransaction_id,0)=0 AND @ForRecall=0 AND @ForReview=1)      -- to preview posting
OR  (I.Status<>3 AND @ForRecall=1 AND @ForReview=0)                       -- for recall
OR  (I.Status=3 AND @ForRecall=1  )))                      		  -- to preview recall
AND P.StatusInd <> '999'
END
GO
