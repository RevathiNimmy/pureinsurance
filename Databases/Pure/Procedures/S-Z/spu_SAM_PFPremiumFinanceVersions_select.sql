CREATE PROCEDURE spu_SAM_PFPremiumFinanceVersions_select      
    @nPremiumFinanceCnt INT      
AS      
SELECT      
        P.pfPrem_finance_cnt,     
        P.pfPrem_finance_version,     
        P.startdate,     
        P.amounttofinance,    
        P.TotalCost,     
        CAST(P.StatusInd As INTEGER) As StatusInd,      
        P.AutoGeneratedPlanRef    
    FROM      
        PFPremiumFinance P      
    WHERE      
     P.pfPrem_finance_cnt = @nPremiumFinanceCnt    
GO

