CREATE PROCEDURE spu_Get_Finance_Plan_Details
    @sFinancePlanRef VARCHAR(50)
AS

SELECT  
    pf.shortname, 
    pfr.mnemonic, 
    s.code, 
    pfp.AutoGeneratedPlanRef, 
    pt.code, 
    p.resolved_name, 
    ppc.party_title_code, 
    ppc.forename, 
    p.name, 
    pfp.ClientAddr1, 
    pfp.ClientAddr2, 
    pfp.ClientAddr3, 
    pfp.ClientAddr4, 
    pfp.ClientTown, 
    pfp.ClientPCode, 
    ISNULL(i1.cover_start_date, i2.cover_start_date),
    ISNULL(i1.renewal_date, i2.renewal_date),
    pfp.BankSortCode, 
    pfp.BankAccountNo, 
    pfp.BankAccountName, 
    pfp.AmountToFinance, 
    pfp.deposit, 
    pfp.NoOfInstallments, 
    pfp.OthInstallments,
    pfr.ProductFamily
FROM pfpremiumfinance pfp
JOIN pfscheme pfs
    ON pfs.schemeno = pfp.schemeno 
    AND pfs.schemeversion = pfp.schemeversion
JOIN party pf
    ON pf.party_cnt = pfs.party_cnt
JOIN party p
    ON p.party_cnt = pfp.clientid
JOIN source s
    ON s.source_id = pfp.companyno 
JOIN party_address_usage pau
    ON pau.party_cnt = p.party_cnt
LEFT JOIN insurance_file i1
    ON i1.insurance_file_cnt = pfp.insurance_file_cnt
LEFT JOIN pftransaction_id pft
    JOIN insurance_file i2
        ON i2.insurance_file_cnt = pft.insurance_file_cnt
    ON pft.pfprem_finance_cnt = pfp.pfprem_finance_cnt
    AND pft.pfprem_finance_version = pfp.pfprem_finance_version
JOIN pfrf pfr
    ON pfr.pfrf_id = pfp.pfrf_id
JOIN party_type pt
    ON pt.party_type_id = p.party_type_id
LEFT JOIN party_personal_client ppc
    ON ppc.party_cnt = p.party_cnt
WHERE RTRIM(pfp.FinanceCollatedPlanRef) = RTRIM(@sFinancePlanRef)

GO
